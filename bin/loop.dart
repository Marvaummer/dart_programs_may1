void main(){
  ///for loop
  for(int i=1;i<=10;i++){
    print(i);


  }
  print("------------------");
  for(int i=10;i>=1;i--) {
    print(i);
  }

  ///while loop


  print("--------------");
  int x=0;
  while (x<10){
    x++;
    print(x);
  }

  print("--------------");
  int y=11;
  while (y>0){
    y--;
    print(y);
  }


  ///do while


  print("---------");
  int a=10;
  do {
    print(a);
    a--;
  }while(a<=1);

}